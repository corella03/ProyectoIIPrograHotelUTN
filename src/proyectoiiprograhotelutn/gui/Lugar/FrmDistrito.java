    public void cargarPaises(){
        cbxPais.removeAllItems();
        PaisBO paisbo = new PaisBO();
        ArrayList<Pais> listaPaises = paisbo.cargarPaises();
        for (Pais p : listaPaises) {
            paises.addElement(p);
        }
    }
    public void cargarProvincias(){///Preguntar
        cbxProvincia.removeAllItems();
        ProvinciaBO provinciabo = new ProvinciaBO();
        ArrayList<Provincia> listaProvincias = provinciabo.cargarProvinciasDePais(((Pais) cbxPais.getSelectedItem()).getId());
        for (Provincia p : listaProvincias) {
            provincias.addElement(p);
        }
    }
    public void cargarCantones(int numberOfProvinces){///Preguntar
        cbxCanton.removeAllItems();
        if(numberOfProvinces > 0) {
            CantonBO cantonbo = new CantonBO();
            ArrayList<Canton> listaCantones = cantonbo.cargarCantonesDeProvincia(((Provincia) cbxProvincia.getSelectedItem()).getId());
            for (Canton c : listaCantones) {
                cantones.addElement(c);
            }
        }
    }
    public void registrarDistrito(){
        lblErrorDistrito.setText("");
        try {
            distrito.setId(this.distrito.getId());
            Distrito distritoNuevo = new Distrito();
            distritoNuevo.setNombre(txtNombreDistrito.getText().trim().toLowerCase());
            distritoNuevo.setCanton((Canton) cbxCanton.getSelectedItem());
            DistritoBO distritobo = new DistritoBO();
            if (distritobo.registrarDistrito(distritoNuevo)) {
                lblErrorDistrito.setText("Distrito registrado con Ã©xito.");
            } else {
                lblErrorDistrito.setText("Intente nuevamente.");
            }
        } catch (MiError ex) {
            lblErrorDistrito.setText(ex.getMessage());
        } catch (Exception ex) {
            lblErrorDistrito.setText("Llamar a TI ...xD");
        }
    }
